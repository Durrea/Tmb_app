    private void jPanel_BuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel_BuscarMouseClicked
        // TODO add your handling code here:
        //jText_Placa.setText("Hola Mundo");
        ValidadorCadenas val = new ValidadorCadenas();
        if(!this.jText_Buscar.getText().equalsIgnoreCase("")&&!this.fechaInforme.getEditor().getText().equalsIgnoreCase(""))
        {
            if(val.ValidarCadenasPlaca(this.jText_Buscar.getText())!=0)
            {
                ParqueaderoMes obj = new ParqueaderoMes();
                String res = obj.BuscadorPlacaMensual(Conexion.obtener(), this.jText_Buscar.getText());
                if(!res.equalsIgnoreCase("Registrado"))
                {
                    //no existe un vehiculo con esa placa en mensual
                    JOptionPane.showMessageDialog(null, "No existe un vehiculo registrado en parqueadero mensual con esta placa");
                    DefaultListModel modelo = new DefaultListModel();
                    this.jList1.setModel(modelo);
                    this.jText_Buscar.setText("");
                    this.fechaInforme.getEditor().setText("");
                }
                else
                {
                    ArrayList<String> entradas = new ArrayList();
                    entradas = obj.InformeEntradasMensual(Conexion.obtener(), jText_Buscar.getText(), fechaInforme.getEditor().getText());
                    if(entradas.size() != 0)
                    {
                        DefaultListModel modelo = new DefaultListModel();
                        modelo.addElement("FECHA Y HORA INGRESO");
                        for(int i=0;i<entradas.size();i++)
                        {
                            modelo.addElement(entradas.get(i));
                        }
                        this.jList1.setModel(modelo);
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(null, "No se encontraron resultados");
                        DefaultListModel modelo = new DefaultListModel();
                        this.jList1.setModel(modelo);
                        this.jText_Buscar.setText("");
                        this.fechaInforme.getEditor().setText("");
                    }
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Formato de placa incorrecto");
                DefaultListModel modelo = new DefaultListModel();
                this.jList1.setModel(modelo);
                this.jText_Buscar.setText("");
                this.fechaInforme.getEditor().setText("");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Debe llenar los campos");
            DefaultListModel modelo = new DefaultListModel();
            this.jList1.setModel(modelo);
            this.jText_Buscar.setText("");
            this.fechaInforme.getEditor().setText("");
        }
    }//GEN-LAST:event_jPanel_BuscarMouseClicked

